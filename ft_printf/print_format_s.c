#include "ft_printf.h"

int	print_format_s(char *s)
{
	size_t	i;

	if (s == NULL)
	{
		write(1, &"(null)", 6);
		return (6);
	}
	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	return (i);
}
